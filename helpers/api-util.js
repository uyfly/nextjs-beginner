export async function getAllEvents() {
  const response = fetch(
    "https://nextjs-course-70e3e-default-rtdb.firebaseio.com/events.json"
  );
  const data = await response.json();

  const events = [];

  for (const key in data) {
    events.push({
      id: key,
      ...data[key],
    });
  }

  return events;
}
